dias_trabalhados = int(input("Insira o total de dias trabalhados em um período de 30 dias:"))
horas_trabalhadas = dias_trabalhados * 8
valor_salário = horas_trabalhadas * 25
print(f"Seu total de dias trabalhados foi de {dias_trabalhados}, sendo assim durante um mês você trabalhou durante {horas_trabalhadas} horas, logo seu salário será de {valor_salário}")